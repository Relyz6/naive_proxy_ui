const express = require('express');
const fs = require('fs');
const path = require('path');
const { exec } = require('child_process');
const crypto = require('crypto');

const app = express();
const PORT = process.env.PORT || 3000;
const DB_PATH = path.join(__dirname, 'db.json');

app.use(express.json());
app.use(express.static(path.join(__dirname, 'public')));

// ==========================================
// DB Helper Functions
// ==========================================
function readDB() {
  try {
    if (!fs.existsSync(DB_PATH)) {
      // Re-create if missing
      const defaults = {
        settings: {
          serverHost: "yourserver.com",
          serverPort: 8443,
          vpnTomlPath: "/opt/trusttunnel/vpn.toml",
          credentialsTomlPath: "./credentials.toml",
          restartCommand: "sudo systemctl restart trusttunnel",
          customSni: "",
          skipVerification: false,
          dnsUpstreams: ["1.1.1.1", "8.8.8.8"]
        },
        users: []
      };
      fs.writeFileSync(DB_PATH, JSON.stringify(defaults, null, 2));
      return defaults;
    }
    const data = fs.readFileSync(DB_PATH, 'utf-8');
    return JSON.parse(data);
  } catch (err) {
    console.error('Error reading db.json:', err);
    return { settings: {}, users: [] };
  }
}

function writeDB(data) {
  try {
    fs.writeFileSync(DB_PATH, JSON.stringify(data, null, 2));
    return true;
  } catch (err) {
    console.error('Error writing db.json:', err);
    return false;
  }
}

// ==========================================
// TOML Credential Sync Generator
// ==========================================
function syncCredentials(dbData) {
  const settings = dbData.settings;
  const filePath = path.resolve(settings.credentialsTomlPath || './credentials.toml');
  
  // Filter only enabled users
  const activeUsers = dbData.users.filter(u => u.enabled);
  
  let tomlContent = `# Generated by TrustTunnel Web Panel
# Active Credentials Count: ${activeUsers.length}
# Last Updated: ${new Date().toISOString()}

`;

  if (activeUsers.length === 0) {
    tomlContent += `client = []\n`;
  } else {
    activeUsers.forEach(user => {
      // Escape quotes to prevent TOML parsing errors
      const safeUser = user.username.replace(/"/g, '\\"');
      const safePass = user.password.replace(/"/g, '\\"');
      tomlContent += `[[client]]\nusername = "${safeUser}"\npassword = "${safePass}"\n\n`;
    });
  }

  try {
    const dir = path.dirname(filePath);
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
    }
    fs.writeFileSync(filePath, tomlContent, 'utf-8');
    console.log(`Successfully synchronized credentials to: ${filePath}`);
    return { success: true, filePath };
  } catch (err) {
    console.error(`Failed to write credentials TOML to ${filePath}:`, err);
    return { success: false, error: err.message };
  }
}

// ==========================================
// Service Restart Trigger
// ==========================================
function triggerRestart(settings, callback) {
  const cmd = settings.restartCommand;
  if (!cmd || cmd.trim() === "") {
    return callback(null, "No restart command configured. Changes saved locally.");
  }
  
  console.log(`Executing restart command: "${cmd}"`);
  exec(cmd, (error, stdout, stderr) => {
    if (error) {
      console.error(`Restart command failed: ${error.message}`);
      // Gracefully return error so it can be shown in settings / logs
      return callback(error, stdout || stderr || error.message);
    }
    console.log(`Restart command succeeded!`);
    return callback(null, stdout || "Service restarted successfully.");
  });
}

// ==========================================
// RFC 9000 VarInt & TLV Deep-Link Encoder
// ==========================================
function encodeVarInt(value) {
  if (value < 64) {
    return Buffer.from([value]);
  } else if (value < 16384) {
    return Buffer.from([
      0x40 | (value >> 8),
      value & 0xFF
    ]);
  } else if (value < 1073741824) {
    return Buffer.from([
      0x80 | (value >> 24),
      (value >> 16) & 0xFF,
      (value >> 8) & 0xFF,
      value & 0xFF
    ]);
  } else {
    const buf = Buffer.alloc(8);
    buf.writeBigUInt64BE(BigInt(value));
    buf[0] |= 0xC0;
    return buf;
  }
}

function encodeTLV(tag, valueBuf) {
  const tagBuf = encodeVarInt(tag);
  const lenBuf = encodeVarInt(valueBuf.length);
  return Buffer.concat([tagBuf, lenBuf, valueBuf]);
}

function encodeStringField(tag, val) {
  return encodeTLV(tag, Buffer.from(val, 'utf-8'));
}

function encodeBoolField(tag, val) {
  return encodeTLV(tag, Buffer.from([val ? 0x01 : 0x00]));
}

function encodeVarIntField(tag, val) {
  return encodeTLV(tag, encodeVarInt(val));
}

function encodeStringArrayField(tag, stringArray) {
  const bufs = [];
  for (const str of stringArray) {
    const strBuf = Buffer.from(str, 'utf-8');
    bufs.push(encodeVarInt(strBuf.length));
    bufs.push(strBuf);
  }
  return encodeTLV(tag, Buffer.concat(bufs));
}

function base64url(buffer) {
  return buffer.toString('base64')
    .replace(/\+/g, '-')
    .replace(/\//g, '_')
    .replace(/=/g, '');
}

let cachedDerBuffer = null;
function getDerCertificate() {
  if (cachedDerBuffer) return cachedDerBuffer;
  try {
    const paths = [
      '/opt/trusttunnel/certs/cert.pem',
      path.join(__dirname, 'certs/cert.pem')
    ];
    
    let certPath = null;
    for (const p of paths) {
      if (fs.existsSync(p)) {
        certPath = p;
        break;
      }
    }
    
    if (!certPath) return null;
    
    const pem = fs.readFileSync(certPath, 'utf-8');
    const base64Data = pem
      .replace(/-----BEGIN CERTIFICATE-----/, '')
      .replace(/-----END CERTIFICATE-----/, '')
      .replace(/\s/g, '');
    cachedDerBuffer = Buffer.from(base64Data, 'base64');
    return cachedDerBuffer;
  } catch (err) {
    console.error('Error reading/parsing self-signed certificate:', err);
    return null;
  }
}

function generateDeepLink(user, settings) {
  const host = settings.serverHost || "yourserver.com";
  const port = parseInt(settings.serverPort) || 8443;
  const address = `${host}:${port}`;
  
  const chunks = [];
  
  // Tag 0x00: version (VarInt) - Version 1 matches DEEP_LINK.md status
  chunks.push(encodeVarIntField(0x00, 1));
  
  // Tag 0x01: hostname (String)
  chunks.push(encodeStringField(0x01, host));
  
  // Tag 0x02: addresses (String) - multiple possible, we push one
  chunks.push(encodeStringField(0x02, address));
  
  // Tag 0x05: username (String)
  chunks.push(encodeStringField(0x05, user.username));
  
  // Tag 0x06: password (String)
  chunks.push(encodeStringField(0x06, user.password));
  
  // Tag 0x07: skip_verification (Bool)
  if (settings.skipVerification) {
    chunks.push(encodeBoolField(0x07, true));
  }
  
  // Tag 0x08: certificate (Bytes) - Embed self-signed certificate so that the app trusts it automatically!
  const derBuffer = getDerCertificate();
  if (derBuffer) {
    chunks.push(encodeTLV(0x08, derBuffer));
  }
  
  // Tag 0x0c: name (String) - Server display name
  const displayName = user.notes ? `${user.username} (${user.notes})` : user.username;
  chunks.push(encodeStringField(0x0c, displayName));
  
  // Tag 0x03: custom_sni (String) - if set
  if (settings.customSni && settings.customSni.trim() !== "") {
    chunks.push(encodeStringField(0x03, settings.customSni.trim()));
  }
  
  // Tag 0x0d: dns_upstreams (String[]) - if set
  if (settings.dnsUpstreams && settings.dnsUpstreams.length > 0) {
    const validDns = settings.dnsUpstreams.filter(dns => dns && dns.trim() !== "");
    if (validDns.length > 0) {
      chunks.push(encodeStringArrayField(0x0d, validDns));
    }
  }
  
  const payloadBuf = Buffer.concat(chunks);
  const encodedPayload = base64url(payloadBuf);
  
  return `tt://?${encodedPayload}`;
}

// ==========================================
// Client TOML Config Exporter
// ==========================================
function generateClientTOML(user, settings) {
  const host = settings.serverHost || "yourserver.com";
  const port = parseInt(settings.serverPort) || 8443;
  const address = `${host}:${port}`;
  
  let toml = `# TrustTunnel Client Configuration
# User: ${user.username}
# Generated: ${new Date().toISOString()}

[client]
server_address = "${address}"
username = "${user.username}"
password = "${user.password}"
mode = "general"
skip_verification = ${settings.skipVerification ? 'true' : 'false'}
`;

  if (settings.customSni && settings.customSni.trim() !== "") {
    toml += `custom_sni = "${settings.customSni.trim()}"\n`;
  }
  
  toml += `\n[dns]\n`;
  if (settings.dnsUpstreams && settings.dnsUpstreams.length > 0) {
    const dnsList = settings.dnsUpstreams.filter(dns => dns && dns.trim() !== "").map(dns => `"${dns}"`).join(', ');
    toml += `upstreams = [${dnsList}]\n`;
  } else {
    toml += `upstreams = ["1.1.1.1", "8.8.8.8"]\n`;
  }
  
  return toml;
}

