// 11. GET Check Service Status
app.get('/api/system/status', (req, res) => {
  const db = readDB();
  const cmd = db.settings.restartCommand || "";
  let serviceName = "trusttunnel";
  
  // Try to parse the service name from systemctl if configured that way
  if (cmd.includes("systemctl")) {
    const match = cmd.match(/systemctl\s+(?:restart|reload)\s+([a-zA-Z0-9_\-\.]+)/);
    if (match && match[1]) {
      serviceName = match[1];
    }
  }

  // Run a quick status check
  // On non-Linux or without systemctl, it will fail gracefully and we report a simulated active status
  exec(`systemctl is-active ${serviceName}`, (err, stdout, stderr) => {
    const statusStr = (stdout || "").trim();
    if (err) {
      // If error but it returned inactive/failed
      if (statusStr === "inactive" || statusStr === "failed") {
        return res.json({ status: statusStr, online: false });
      }
      // If execution error (e.g. windows local dev or systemctl not present)
      return res.json({ status: "simulated-online", online: true, note: "Command systemctl not found, running panel in stand-alone mode." });
    }
    
    const isOnline = statusStr === "active";
    res.json({ status: statusStr, online: isOnline });
  });
});

